#program to print numbers from 1 to N using while loop
# Read value of N from user
n = int(input("Enter a number: "))

# Initialize counter
i = 1

# Use while loop to print numbers from 1 to N
while i <= n:
    print(i)
    i += 1   # Increase counter by 1



