import pandas as pd

marks = [45, 67, 89, 34, 90, 55, 72]
subjects = ['Math', 'Physics', 'Chemistry', 'English', 'CS', 'Biology', 'History']

s = pd.Series(marks, index=subjects)

# Subjects with marks > 70
print(s[s > 70])

# Replace marks < 40 with "Fail"
s_replaced = s.copy()
s_replaced[s < 40] = "Fail"
print(s_replaced)

# Count subjects above average
avg = s.mean()
count = (s > avg).sum()
print(count)

salary = [25000, 32000, 28000, 40000, 50000]
emp = ['A', 'B', 'C', 'D', 'E']

s = pd.Series(salary, index=emp)

# Increase 10% where salary < 30000
s[s < 30000] *= 1.10

# Decrease 5% where salary > 45000
s[s > 45000] *= 0.95

# Total salary expense
print(s.sum())

data = [10, None, 30, None, 50, 60, None]
s = pd.Series(data)

# Count missing before
before = s.isna().sum()

# Fill with mean
s = s.fillna(s.mean())

# Count after
after = s.isna().sum()

# Drop remaining nulls
s = s.dropna()

print(before, after)

marks = [95, 82, 67, 58, 73, 49, 88]
students = ['S1','S2','S3','S4','S5','S6','S7']

s = pd.Series(marks, index=students)

def grade(x):
    if x >= 90:
        return 'A'
    elif x >= 75:
        return 'B'
    elif x >= 60:
        return 'C'
    else:
        return 'D'

grades = s.apply(grade)

# Count each grade
print(grades.value_counts())

s1 = pd.Series([10, 20, 30], index=['A', 'B', 'C'])
s2 = pd.Series([5, 15, 25], index=['B', 'C', 'D'])

# Addition
result = s1 + s2
print(result)

# Replace NaN with 0 and recompute
result_fixed = s1.add(s2, fill_value=0)
print(result_fixed)

scores = [88, 92, 79, 93, 85]
players = ['P1','P2','P3','P4','P5']

s = pd.Series(scores, index=players)

# Rank
ranks = s.rank(ascending=False)

