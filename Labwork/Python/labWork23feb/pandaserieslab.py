data = [5, None, 15, 25, None, 35, 45]
s = pd.Series(data)

result = (
    s.fillna(s.median())
     .loc[lambda x: x > 20]
     .sort_values(ascending=False)
     .reset_index(drop=True)
)

print(result)

sales = [200, 450, 300, 150, 500, 700, 100]
days = ['Mon','Tue','Wed','Thu','Fri','Sat','Sun']

s = pd.Series(sales, index=days)

# Best & worst day
best = s.idxmax()
worst = s.idxmin()

# Total & average
total = s.sum()
avg = s.mean()

# Apply 20% bonus where sales > 400
bonus = s.copy()
bonus[s > 400] *= 1.2

# Sort descending
sorted_sales = s.sort_values(ascending=False)

print(best, worst)
print(total, avg)
print(bonus)
print(sorted_sales)