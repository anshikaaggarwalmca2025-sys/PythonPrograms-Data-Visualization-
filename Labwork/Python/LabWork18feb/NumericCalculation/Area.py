#program to calculate area of a circle and parameter of circle
#input of radius of circle
radius = float(input("Enter the radius of circle: "))
#calculating area of circle
area = 3.14 * radius * radius
#calculating parameter of circle
parameter = 2 * 3.14 * radius
#printing area and parameter of circle
print("Area of circle: ", area)
print("Parameter of circle: ", parameter)